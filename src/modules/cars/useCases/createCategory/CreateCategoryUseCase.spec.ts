describe("Create Category", () => {
  it("The sum 2 + 2 should be equal 4", () => {
    const sum = 2 + 2;
    const result = 4;

    expect(sum).toBe(result);
  });

  it("The sum 2 + 2 should not be equal 5", () => {
    const sum = 2 + 2;
    const result = 5;

    expect(sum).not.toBe(result);
  });
});
